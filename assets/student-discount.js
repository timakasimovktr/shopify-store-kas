$(document).ready(() => {
  $('.btn-get-student-discount-code').click((e) => {
    e.preventDefault();

    $('#popup-student-beans')
    .stop()
    .css("display", "flex").hide()
    .fadeIn();

  });

  $('#popup-student-beans').click((e) => {
    $('#popup-student-beans')
    .stop()
    .fadeOut();
  });
});